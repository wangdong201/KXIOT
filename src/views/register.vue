<template>
  <div class="register">
    <vue-particles
          color="#21fcfa"
          :particleOpacity="0.7"
          :particlesNumber="39"
          shapeType="circle"
          :particleSize="5"
          linesColor="#409EFF"
          :linesWidth="2"
          :lineLinked="true"
          :lineOpacity="0.4"
          :linesDistance="150"
          :moveSpeed="3"
          :hoverEffect="true"
          hoverMode="grab"
          :clickEffect="false"
          clickMode="push"
